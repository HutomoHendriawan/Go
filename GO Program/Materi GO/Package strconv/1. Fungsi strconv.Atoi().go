// Fungsi ini digunakan untuk konversi data dari tipe string ke int. strconv.Atoi() menghasilkan 2 buah 
// nilai kembalian, yaitu hasil konversi dan error (jika konversi sukses, maka error berisi nil).

package main

import "fmt"
import "strconv"

func main() {
		var str = "124"
		var num, err = strconv.Atoi(str)

		if err == nil {
				fmt.Println(num) // 124
		}
}

